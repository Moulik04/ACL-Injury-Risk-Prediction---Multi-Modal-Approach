# 2. Preprocess Data
scaler_sp = StandardScaler()
X_train_sp_scaled = scaler_sp.fit_transform(X_train_sp)
X_test_sp_scaled = scaler_sp.transform(X_test_sp)

# Run for Multimodal Data
train_and_evaluate(models, X_train_sp_scaled, y_train_sp, X_test_sp_scaled, y_test_sp, 'Multimodal', X_train_sp.columns)

