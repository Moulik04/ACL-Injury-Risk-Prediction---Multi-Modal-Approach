import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.preprocessing import StandardScaler, LabelEncoder
from sklearn.ensemble import RandomForestClassifier, GradientBoostingClassifier
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import (accuracy_score, precision_score, recall_score,
                             f1_score, roc_auc_score, confusion_matrix, roc_curve)

# =========================================================
# PART 1: Collegiate Athlete Workload Data Pipeline
# =========================================================
print("--- Training Models on Collegiate Dataset ---")

# 1. Load Data
train_col = pd.read_csv('collegiate_athlete_injury_dataset_train.csv')
val_col = pd.read_csv('collegiate_athlete_injury_dataset_validation.csv')
test_col = pd.read_csv('collegiate_athlete_injury_dataset_test.csv')

# Combine Train and Validation for a larger training pool
X_train_col = pd.concat([train_col, val_col]).drop(columns=['Athlete_ID', 'Injury_Indicator'])
y_train_col = pd.concat([train_col, val_col])['Injury_Indicator']
X_test_col = test_col.drop(columns=['Athlete_ID', 'Injury_Indicator'])
y_test_col = test_col['Injury_Indicator']

# 2. Preprocess Data (Encode Categorical Variables)
cat_cols = ['Gender', 'Position']
for col in cat_cols:
    X_train_col[col] = X_train_col[col].astype(str)
    X_test_col[col] = X_test_col[col].astype(str)
    le = LabelEncoder()
    # Fit on all possible labels to avoid unseen class errors
    le.fit(pd.concat([X_train_col[col], X_test_col[col]]))
    X_train_col[col] = le.transform(X_train_col[col])
    X_test_col[col] = le.transform(X_test_col[col])

# Scale numeric features
scaler_col = StandardScaler()
X_train_col_scaled = scaler_col.fit_transform(X_train_col)
X_test_col_scaled = scaler_col.transform(X_test_col)

# 3. Define Models to Compare
models = {
    'Logistic Regression': LogisticRegression(random_state=42),
    'Random Forest': RandomForestClassifier(random_state=42, n_estimators=100),
    'Gradient Boosting': GradientBoostingClassifier(random_state=42)
}

# 4. Train, Evaluate, and Plot (Collegiate)
def train_and_evaluate(models, X_train, y_train, X_test, y_test, dataset_name, feature_names):
    results = []
    fig_cm, axes = plt.subplots(1, 3, figsize=(16, 5))

    for i, (name, model) in enumerate(models.items()):
        # Train Model
        model.fit(X_train, y_train)
        y_pred = model.predict(X_test)
        y_prob = model.predict_proba(X_test)[:, 1]

        # Calculate Metrics
        acc = accuracy_score(y_test, y_pred)
        prec = precision_score(y_test, y_pred, zero_division=0)
        rec = recall_score(y_test, y_pred)
        f1 = f1_score(y_test, y_pred)
        roc = roc_auc_score(y_test, y_prob)

        results.append({'Model': name, 'Accuracy': acc, 'Precision': prec,
                        'Recall': rec, 'F1-Score': f1, 'ROC-AUC': roc})

        # Plot Confusion Matrix
        cm = confusion_matrix(y_test, y_pred)
        sns.heatmap(cm, annot=True, fmt='d', cmap='Blues' if dataset_name == 'Collegiate' else 'Oranges', ax=axes[i], cbar=False)
        axes[i].set_title(f'{name} ({dataset_name})')
        axes[i].set_xlabel('Predicted Risk')
        axes[i].set_ylabel('Actual Risk')

    plt.tight_layout()
    plt.savefig(f'{dataset_name}_confusion_matrices.png')
    plt.show()

    # Save Metrics to CSV
    results_df = pd.DataFrame(results)
    results_df.to_csv(f'{dataset_name}_model_metrics.csv', index=False)
    print(f"\n{dataset_name} Evaluation Metrics:")
    print(results_df.to_string(index=False))

    # Plot ROC Curves
    plt.figure(figsize=(8, 6))
    for name, model in models.items():
        y_prob = model.predict_proba(X_test)[:, 1]
        fpr, tpr, _ = roc_curve(y_test, y_prob)
        auc = roc_auc_score(y_test, y_prob)
        plt.plot(fpr, tpr, label=f'{name} (AUC = {auc:.2f})')

    plt.plot([0, 1], [0, 1], 'k--', color='grey')
    plt.xlabel('False Positive Rate')
    plt.ylabel('True Positive Rate')
    plt.title(f'ROC Curves Comparison - {dataset_name} Dataset')
    plt.legend(loc='lower right')
    plt.grid(alpha=0.3)
    plt.tight_layout()
    plt.savefig(f'{dataset_name}_roc_curves.png')
    plt.show()

    # Plot Feature Importance (Extracting from Random Forest)
    rf_model = models['Random Forest']
    importances = rf_model.feature_importances_
    indices = np.argsort(importances)[::-1]

    plt.figure(figsize=(10, 6))
    plt.title(f"Top Driving Factors for ACL Injury Risk ({dataset_name})")
    plt.bar(range(X_train.shape[1]), importances[indices], align="center", color='skyblue' if dataset_name == 'Collegiate' else 'coral', edgecolor='black')
    plt.xticks(range(X_train.shape[1]), [feature_names[i] for i in indices], rotation=45 if dataset_name == 'Collegiate' else 90, ha='right')
    plt.xlim([-1, X_train.shape[1]])
    plt.ylabel("Relative Importance")
    plt.tight_layout()
    plt.savefig(f'{dataset_name}_feature_importance.png')
    plt.show()

# Run for Collegiate Data
train_and_evaluate(models, X_train_col_scaled, y_train_col, X_test_col_scaled, y_test_col, 'Collegiate', X_train_col.columns)


# =========================================================
# PART 2: Sports Multimodal (Wearable/Sensor) Data Pipeline
# =========================================================
print("\n--- Training Models on Multimodal Sensor Dataset ---")

# 1. Load Data
train_sp = pd.read_csv('sports_multimodal_data_train.csv')
val_sp = pd.read_csv('sports_multimodal_data_validation.csv')
test_sp = pd.read_csv('sports_multimodal_data_test.csv')

X_train_sp = pd.concat([train_sp, val_sp]).drop(columns=['injury_risk'])
y_train_sp = pd.concat([train_sp, val_sp])['injury_risk']
X_test_sp = test_sp.drop(columns=['injury_risk'])
